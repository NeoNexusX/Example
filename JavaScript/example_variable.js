// Number type variable
var num = 42;
console.log(num);
var floatNum = 3.14;
console.log(floatNum);

//binary type variable
var binNum = 0b1010; // binary for 10
console.log(binNum);

//octal type variable
var octNum = 0o52; // octal for 42
console.log(octNum);

//hexadecimal type variable
var hexNum = 0x2A; // hexadecimal for 42
console.log(hexNum);

